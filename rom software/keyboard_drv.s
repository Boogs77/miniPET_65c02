INIT_KEYBOARD:
  sei
  cld
  
  lda #$00
  sta kb_wptr
  sta kb_rptr
  sta kb_flags
  
  ; **INIT DEBUG**
  sta DEBUG_PTR
  ldx #$7F
@clear:
  lda #$FF
  sta DEBUG_LOG, x
  dex
  bpl @clear

  lda #$01
  sta PCR

  lda #%00000000
  sta DDRAK
  rts

FLUSH_KEYBOARD:
  lda kb_wptr
  sta kb_rptr
  rts


keyboard_interrupt:
  pha
  txa
  pha
  
  lda PORTAK
  tax
  
  lda kb_flags
  and #RELEASE
  beq read_key

  lda kb_flags
  eor #RELEASE
  sta kb_flags
  
  cpx #$12
  beq shift_up
  cpx #$59
  beq shift_up
  jmp exit

shift_up:
  lda kb_flags
  eor #SHIFT
  sta kb_flags
  jmp exit

read_key:
  cpx #$f0
  beq key_release
  cpx #$12
  beq shift_down
  cpx #$59
  beq shift_down

  lda kb_flags
  and #SHIFT
  bne shifted_key

  lda keymap, x
  jmp push_key

shifted_key:
  lda keymap_shifted, x

push_key:
  cmp #'?'
  beq exit
  
  pha
  phx
  ldx DEBUG_PTR
  cpx #$80
  bcs @skip_log
  sta DEBUG_LOG, x
  inc DEBUG_PTR
@skip_log:
  plx
  pla
  
  ; Scrivi nel buffer
  ldx kb_wptr
  sta kb_buffer, x
  inc kb_wptr
  jmp exit

shift_down:
  lda kb_flags
  ora #SHIFT
  sta kb_flags
  jmp exit

key_release:
  lda kb_flags
  ora #RELEASE
  sta kb_flags

exit:
  pla
  tax
  pla
  rts

